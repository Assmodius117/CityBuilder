package builderMk1;

public class PointGen {
	private int worth;
	private String terrian;
	private String owner;
	private String devolpment;
	private String key;
	
	public PointGen(){
		
	}
	
	public int getWorth() {
		return worth;
	}
	public void setWorth(int worth) {
		this.worth = worth;
	}
	public String getTerrian() {
		return terrian;
	}
	public void setTerrian(String terrian) {
		this.terrian = terrian;
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public String getDevolpment() {
		return devolpment;
	}
	public void setDevolpment(String devolpment) {
		this.devolpment = devolpment;
	}
	
	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
	
}
